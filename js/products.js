function generateProductCard(p) {
  const sizeHTML = p.sizes.map(s => `<span class="size-tag">${s}</span>`).join('');
  return `
    <div class="product-card">
      <a href="product-details.html?id=${p.id}" class="product-img-wrap" style="display:block;">
        <img src="${p.img}" alt="${p.name}" class="product-img" loading="lazy" onerror="this.onerror=null; this.src='../images/products/${p.cat}/${p.cat}-01.webp';" />
        <div class="product-badges">
          ${p.discount ? `<span class="badge badge-sale">-${p.discount}</span>` : ''}
        </div>
      </a>
      <div class="product-info">
        <span class="product-cat">${p.cat.toUpperCase()}</span>
        <h3 class="product-name"><a href="product-details.html?id=${p.id}">${p.name}</a></h3>
        <p class="product-desc">${p.desc}</p>
        <div class="product-rating">⭐ ${p.rating} / 5</div>
        <div class="product-sizes">
          <span class="size-label">Sizes:</span>
          ${sizeHTML}
        </div>
        <div class="product-price-row">
          <span class="product-price">${p.price}</span>
          ${p.oldPrice ? `<span class="product-price-old">${p.oldPrice}</span>` : ''}
        </div>
        <div class="product-btns">
          <button class="btn btn-primary btn-sm" onclick="addToCart({id:'${p.id}', name:'${p.name}', price:'${p.price}', img:'${p.img}', size:'${p.sizes[0]}'})">🛒 Add</button>
          <a href="product-details.html?id=${p.id}" class="btn btn-outline-dark btn-sm" style="text-align:center;">View Details</a>
        </div>
      </div>
    </div>
  `;
}

function renderProductsGrid(productsToRender, containerId) {
  const container = document.getElementById(containerId);
  if (!container) return;
  
  if (productsToRender.length === 0) {
    container.innerHTML = '<div style="grid-column: 1/-1; text-align: center; padding: 40px; color: #666;">No products found matching your criteria.</div>';
    return;
  }
  container.innerHTML = productsToRender.map(generateProductCard).join('');
}

function initProductsPage() {
  const gridId = 'product-grid';
  if (!document.getElementById(gridId)) return;

  renderProductsGrid(productData, gridId);

  // Category Filtering
  const filterBtns = document.querySelectorAll('.filter-btn');
  filterBtns.forEach(btn => {
    btn.addEventListener('click', (e) => {
      filterBtns.forEach(b => b.classList.remove('active'));
      e.target.classList.add('active');
      const cat = e.target.dataset.cat;
      if (cat === 'all') {
        renderProductsGrid(productData, gridId);
      } else {
        const filtered = productData.filter(p => p.cat === cat);
        renderProductsGrid(filtered, gridId);
      }
    });
  });

  // Live Search
  const searchInput = document.getElementById('pageSearchInput');
  if (searchInput) {
    searchInput.addEventListener('input', (e) => {
      const q = e.target.value.toLowerCase().trim();
      // Remove active class from categories when searching
      filterBtns.forEach(b => b.classList.remove('active'));
      const activeAll = Array.from(filterBtns).find(b => b.dataset.cat === 'all');
      if (activeAll && q === '') activeAll.classList.add('active');

      const filtered = productData.filter(p => 
        p.name.toLowerCase().includes(q) || 
        p.cat.toLowerCase().includes(q) || 
        p.desc.toLowerCase().includes(q)
      );
      renderProductsGrid(filtered, gridId);
    });
  }
}

function loadProductDetails() {
  const params = new URLSearchParams(window.location.search);
  const id = params.get('id');
  if (!id) return; // Not on details page or missing ID

  const p = productData.find(prod => prod.id === id);
  if (!p) {
    const container = document.getElementById('productDetailsContainer');
    if (container) container.innerHTML = '<div style="text-align:center; padding: 100px;"><h2>Product not found.</h2><a href="products.html" class="btn btn-primary">Back to Products</a></div>';
    return;
  }

  // Populate Details
  const els = {
    img: document.getElementById('pdImg'),
    name: document.getElementById('pdName'),
    price: document.getElementById('pdPrice'),
    oldPrice: document.getElementById('pdOldPrice'),
    discount: document.getElementById('pdDiscount'),
    desc: document.getElementById('pdDesc'),
    rating: document.getElementById('pdRating'),
    cat: document.getElementById('pdCat'),
    sizes: document.getElementById('pdSizes')
  };

  if (els.img) {
    els.img.src = p.img;
    els.img.onerror = function() {
      this.onerror = null;
      this.src = `images/products/${p.cat}/${p.cat}-01.webp`;
    };
  }
  if (els.name) els.name.textContent = p.name;
  if (els.price) els.price.textContent = p.price;
  if (els.oldPrice) els.oldPrice.textContent = p.oldPrice;
  if (els.discount && p.discount) els.discount.textContent = '-' + p.discount;
  if (els.desc) els.desc.textContent = p.desc;
  if (els.rating) els.rating.textContent = '⭐ ' + p.rating + ' / 5 Rating';
  if (els.cat) els.cat.textContent = p.cat.toUpperCase();
  
  if (els.sizes) {
    els.sizes.innerHTML = p.sizes.map((s, idx) => `
      <div class="pd-size-box ${idx === 0 ? 'active' : ''}" onclick="selectPdSize(this, '${s}')">${s}</div>
    `).join('');
    window.selectedPdSize = p.sizes[0];
    window.currentProduct = p; // Global for Add to Cart
  }
}

// Attach size selection globally
window.selectPdSize = function(element, size) {
  document.querySelectorAll('.pd-size-box').forEach(el => el.classList.remove('active'));
  element.classList.add('active');
  window.selectedPdSize = size;
};

// Global Add to Cart wrapper for the details page
window.addPdToCart = function() {
  if (window.currentProduct) {
    addToCart({
      id: window.currentProduct.id,
      name: window.currentProduct.name,
      price: window.currentProduct.price,
      img: window.currentProduct.img,
      size: window.selectedPdSize || window.currentProduct.sizes[0]
    });
  }
};

window.orderPdWhatsApp = function() {
  if (!window.currentProduct) return;
  const size = window.selectedPdSize || window.currentProduct.sizes[0];
  const text = encodeURIComponent(`Hello Kasaudhan Collection,\n\nI want to order:\n\n🛍 Product: ${window.currentProduct.name}\n📏 Size: ${size}\n💰 Price: ${window.currentProduct.price}\n\nPlease contact me.`);
  window.open(`https://wa.me/919696824331?text=${text}`, '_blank');
};

document.addEventListener('DOMContentLoaded', () => {
  initProductsPage();
  loadProductDetails();
});
